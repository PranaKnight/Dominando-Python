C = A * B #Multiplicação
print(C)

C = A / B #Divisão
print(C)

C = A // B #Divisão Inteira
print(C)

C = A % B #Resto
print(C)

C = - A #unário
print(C)

C = A ** B #Potenciação
print(C)

